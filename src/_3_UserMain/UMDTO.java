package _3_UserMain;

public class UMDTO {
	private String id, name, lastday;
	private int ptnum;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLastday() {
		return lastday;
	}
	public void setLastday(String lastday) {
		this.lastday = lastday;
	}
	public int getPtnum() {
		return ptnum;
	}
	public void setPtnum(int ptnum) {
		this.ptnum = ptnum;
	}  
}
